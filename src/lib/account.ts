// 계좌번호 유틸 (순수, 브라우저 안전).
// 저장·송금엔 항상 '숫자만' 사용한다. 하이픈은 화면 표시용 best-effort —
// 한 은행도 상품/시기별로 자릿수·구분이 달라 통장과 100% 일치는 보장하지 않는다(숫자가 정본).
// 패턴에 없거나 자릿수가 넘치면 남는 숫자는 그대로 이어붙인다(숫자 손실 없음).

// 은행별 하이픈 그룹(자릿수). banks.ts의 한글 짧은이름 기준.
const BANK_GROUPS: Record<string, number[]> = {
  카카오뱅크: [4, 2, 7], // 3333-01-1234567
  토스뱅크: [4, 4, 4], // 1000-0000-0000
  케이뱅크: [3, 3, 6], // 100-123-456789
  국민: [6, 2, 6], // 123456-78-901234
  신한: [3, 3, 6], // 110-123-456789
  우리: [4, 3, 6], // 1002-123-456789
  하나: [3, 6, 5], // 123-456789-01234
  농협: [3, 4, 4, 2], // 123-4567-8901-23
  기업: [3, 6, 2, 3], // 123-456789-01-234
  SC제일: [3, 2, 6], // 123-45-678901
  씨티: [3, 3, 6], // 123-456-789012
  대구: [3, 2, 6], // 123-45-678901
  부산: [3, 4, 6], // 123-4567-890123
  경남: [3, 4, 6],
  광주: [3, 3, 6],
  전북: [3, 2, 6],
  제주: [3, 2, 6],
  새마을: [4, 4, 4, 1], // 1234-5678-9012-3
  신협: [4, 4, 4],
  우체국: [6, 2, 6], // 012345-67-890123
  산업: [3, 6, 2], // 123-456789-01
  수협: [3, 4, 6],
}

/** 숫자만 남긴다(하이픈·공백·기타 제거). */
export function onlyDigits(s: string): string {
  return s.replace(/\D/g, '')
}

/**
 * 은행 규칙에 맞춰 하이픈을 넣어 표시용 문자열을 만든다.
 * 입력의 숫자만 취해 그룹대로 끊고, 그룹을 초과하는 숫자는 그대로 뒤에 붙인다.
 * 패턴이 없는 은행은 숫자 그대로(하이픈 없음).
 */
export function formatAccountNo(bank: string, raw: string): string {
  const digits = onlyDigits(raw)
  const groups = BANK_GROUPS[bank]
  if (!groups || !digits) return digits
  const parts: string[] = []
  let i = 0
  for (const g of groups) {
    if (i >= digits.length) break
    parts.push(digits.slice(i, i + g))
    i += g
  }
  if (i < digits.length) parts.push(digits.slice(i)) // 남는 자리 그대로
  return parts.join('-')
}
